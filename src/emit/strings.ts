export function header(file: string){
  return `// @generated by next-prisma-flow-state-engine\n// file: ${file}\n`;
}

export function imp(mod: string, names: string[]){
  return `import { ${names.join(", ")} } from "${mod}";`;
}

export function impType(mod: string, names: string[]){
  return `import type { ${names.join(", ")} } from "${mod}";`;
}

export function exp(names: string[]){
  return `export { ${names.join(", ")} };`;
}

export function expAll(mod: string){
  return `export * from "${mod}";`;
}